    public boolean validateByteArrayElement(byte[] byteArray) {
        long maxSize = 4000000;
        int index = 0;
        return (byteArray[index] & 0xFF) <= maxSize;
    }

    public boolean validarCadena(String calorias) {
        if (calorias.length() < 1 || calorias.length() > 5) {
            return false;
        }
        for (int i = 0; i < calorias.length(); i++) {
            if (!Character.isDigit(calorias.charAt(i))) {
                return false;
            }
        }
        return true;
    }

    public boolean validarNombre(String nombre) {
        String regex = "^[a-zA-Z침칌치칠칤칩칰츼칄칈칍칔0-9\\s]+$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(nombre);
        return matcher.matches();
    }

